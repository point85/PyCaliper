class Operands():
    def __init__(self):
        pass
    
    @staticmethod
    def multOp():    
        return '\xB7'
 
    @staticmethod
    def divOp():    
        return '/'
 
    @staticmethod
    def powOp():    
        return '^'
 
    @staticmethod
    def sqOp():    
        return '\xB2'
 
    @staticmethod
    def cubed():    
        return '\xB3'
 
    @staticmethod
    def lpChar():    
        return '('
 
    @staticmethod
    def rpChar():    
        return ')'
 
    @staticmethod
    def oneChar():    
        return '1'