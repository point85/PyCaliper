class Operands():
    def __init__(self):
        pass
    
    @staticmethod
    def mult():    
        return '\xB7'
 
    @staticmethod
    def div():    
        return '/'
 
    @staticmethod
    def pow():    
        return '^'
 
    @staticmethod
    def sq():    
        return '\xB2'
 
    @staticmethod
    def cubed():    
        return '\xB3'
 
    @staticmethod
    def lp():    
        return '('
 
    @staticmethod
    def rp():    
        return ')'
 
    @staticmethod
    def oneChar():    
        return '1'